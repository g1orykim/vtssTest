/*
 * var_snmp():
 *   This function is called every time the agent gets a request for
 *   a scalar variable that might be found within your mib section
 *   registered above.  It is up to you to do the right thing and
 *   return the correct value.
 *     You should also correct the value of "var_len" if necessary.
 *
 *   Please see the documentation for more information about writing
 *   module extensions, and check out the examples in the examples
 *   and mibII directories.
 */
u_char         *
var_snmp(struct variable *vp,
         oid *name,
         size_t *length,
         int exact, size_t *var_len, WriteMethod **write_method)
{
    /*
     * variables we may use later
     */
    /*
     * static long long_ret;
     * static u_long ulong_ret;
     * static u_char string[SPRINT_MAX_LEN];
     * static oid objid[MAX_OID_LEN];
     * static struct counter64 c64;
     */

    static u_long ulong_ret;

    if (header_generic(vp, name, length, exact, var_len, write_method)
        == MATCH_FAILED) {
        return NULL;
    }

    /*
     * this is where we do the value assignments for the mib results.
     */
    switch (vp->magic) {
    case SNMPOUTGENERRS:
    case SNMPINPKTS:
    case SNMPINGENERRS:
    case SNMPINTOTALSETVARS:
    case SNMPINTRAPS:
    case SNMPINGETNEXTS:
    case SNMPINBADCOMMUNITYNAMES:
    case SNMPINTOTALREQVARS:
    case SNMPINNOSUCHNAMES:
    case SNMPINGETRESPONSES:
    case SNMPINASNPARSEERRS:
    case SNMPINTOOBIGS:
    case SNMPINREADONLYS:
    case SNMPOUTGETREQUESTS:
    case SNMPINBADVALUES:
    case SNMPOUTTOOBIGS:
    case SNMPOUTGETRESPONSES:
    case SNMPOUTPKTS:
    case SNMPINBADCOMMUNITYUSES:
    case SNMPINSETREQUESTS:
    case SNMPOUTTRAPS:
    case SNMPSILENTDROPS:
    case SNMPOUTGETNEXTS:
    case SNMPINBADVERSIONS:
    case SNMPOUTSETREQUESTS:
    case SNMPOUTNOSUCHNAMES:
    case SNMPOUTBADVALUES:
    case SNMPPROXYDROPS:
    case SNMPINGETREQUESTS: {
        ulong_ret  = snmp_get_statistic(vp->magic);
        return (u_char *) &ulong_ret;
    }
    case SNMPENABLEAUTHENTRAPS: {
        snmp_conf_t snmp_conf;

        *write_method = write_snmpEnableAuthenTraps;
        snmp_mgmt_snmp_conf_get(&snmp_conf);
        ulong_ret = (u_long)snmp_conf.trap_authen_fail ? 1 : 2;
        return (u_char *) &ulong_ret;
    }
    default:
        DEBUGMSGTL(("snmpd", "unknown sub-id %d in var_snmp\n",
                    vp->magic));
    }
    return NULL;
}

int
write_snmpEnableAuthenTraps(int action,
                            u_char *var_val,
                            u_char var_val_type,
                            size_t var_val_len,
                            u_char *statP, oid *name, size_t name_len)
{
    ulong      *buf, *old_buf;
    int         max_size;
    long        intval;

    buf      = &now_snmp_conf.trap_authen_fail;
    old_buf  = &old_snmp_conf.trap_authen_fail;
    max_size = sizeof(long);
    intval   = *((long *) var_val);

    switch (action) {
    case RESERVE1: {
        if (var_val_type != ASN_INTEGER) {
            snmp_log(LOG_ERR,
                     "write to snmpEnableAuthenTraps: not ASN_INTEGER\n");
            return SNMP_ERR_WRONGTYPE;
        }
        if (var_val_len > max_size) {
            snmp_log(LOG_ERR,
                     "write to snmpEnableAuthenTraps: bad length\n");
            return SNMP_ERR_WRONGLENGTH;
        }
        if (intval != 1 && intval != 2) {
            snmp_log(LOG_ERR,
                     "write to snmpEnableAuthenTraps: bad value\n");
            return SNMP_ERR_WRONGVALUE;
        }
        break;
    }
    case RESERVE2: {
        /*
         * Allocate memory and similar resources
         */
        break;
    }
    case FREE: {
        /*
         * Release any resources that have been allocated
         */
        break;
    }
    case ACTION: {
        /*
         * The variable has been stored in 'value' for you to use,
         * and you have just been asked to do something with it.
         * Note that anything done here must be reversable in the UNDO case
         */
        /*
         * Save to current configuration
         */
        snmp_conf_t snmp_conf;

        *buf = *((u_long *) var_val);
        snmp_mgmt_snmp_conf_get(&snmp_conf);
        snmp_conf.trap_authen_fail = (*buf == 1 ? 1 : 0);
        if (snmp_mgmt_snmp_conf_set(&snmp_conf) != VTSS_OK) {
            return SNMP_ERR_RESOURCEUNAVAILABLE;
        }
        break;
    }
    case UNDO: {
        /*
         * Back out any changes made in the ACTION case
         */
        /*
         * Restore current configuration form old configuration
         */
        snmp_conf_t snmp_conf;

        *buf = *old_buf;
        snmp_mgmt_snmp_conf_get(&snmp_conf);
        snmp_conf.trap_authen_fail = (*buf == 1 ? 1 : 0);
        if (snmp_mgmt_snmp_conf_set(&snmp_conf) != VTSS_OK) {
            return SNMP_ERR_UNDOFAILED;
        }
        break;
    }
    case COMMIT: {
        /*
         * Things are working well, so it's now safe to make the change
         * permanently.  Make sure that anything done here can't fail!
         */
        /*
         * Update old configuration
         */
        *old_buf = *buf;
        break;
    }
    }
    return SNMP_ERR_NOERROR;
}

